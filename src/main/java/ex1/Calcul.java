package ex1;

/** Classe de calcul pour réaliser des opérations entre entiers
 *
 */
public class Calcul {

	/** Calcul la somme de deux entiers
	 * @param a premier entier
	 * @param b deuxième entier
	 * @return  somme des deux entiers
	 * */
	public int somme(int a, int b){
		return a+b;
	}

	/** Calcul la soustraction de deux entiers
	 * @param a premier entier
	 * @param b deuxième entier
	 * @return  soustraction de a - b
	 *
	 * */
	public int soustraction(int a, int b){
		return a-b;
	}
}
